-- Total Cases vs Total Deaths
-- Shows likelihood of dying if you contract covid in your country

Select Location, date, total_cases,total_deaths, (total_deaths/total_cases)*100 as DeathPercentage
From PortfolioProject..CovidDeaths$
Where location like '%states%'     -- Those locations having word states in it
and continent is not null 
order by 1,2                       -- Order by Location and then date


-- Analysis : We can see that initially, we had less death percentage in the month of february and in the month of april - may, it had become maximum. After that is slowly goes on decreasing.


-- Total Cases vs Population
